// Function to get NFT data by ID
export function getNftById(id) {
    // Use modulo to loop through available NFTs if the ID is out of range
    const nftId = id % Object.keys(nftTraits).length;
    return {
        ...nftTraits[nftId],
        id: nftId,
        image: galleryImages[nftId % galleryImages.length] // Fallback to cycle through available images
    };
}

// Function to get all NFTs
export function getAllNfts() {
    return Object.entries(nftTraits).map(([id, data]) => ({
        id: parseInt(id),
        ...data,
        image: galleryImages[parseInt(id) % galleryImages.length]
    }));
}

// Function to get gallery images with their corresponding NFT data
export function getGalleryImages() {
    // Skip the first image (index 0) which is the 'About Me' image
    return galleryImages.slice(1).map((image, index) => ({
        src: image,
        alt: `Quants NFT ${index + 1}`,
        nftId: (index + 1) % Object.keys(nftTraits).length, // Add 1 to skip the 'About Me' NFT
        nftData: getNftById((index + 1) % Object.keys(nftTraits).length)
    }));
}
